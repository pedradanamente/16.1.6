<h1 >Drop (ou Apagar Objeto)</h1>
<hr/>
<button class="mostrar" onclick="MENU_OPENCLOSE()">DETALHES</button>
<div id="details" class="details">
	<ul>
		<li><strong>Subconjunto:</strong> DDL - Linguagem de Definição de Dados</li>
		<li><strong>Função:</strong> Apagar Objeto</li>
		<li><strong>Descrição do comando:</strong> apaga um objeto do banco de dados, por exemplo um indice, tabela ou banco.</li>
		<li>
			<strong>Exemplo:</strong>	
			<ul>
				<li>
					<h3><strong>The DROP INDEX Statement</strong></h3>
					<p>The DROP INDEX statement is used to delete an index in a table.</p>
					<p><strong>DROP INDEX Syntax for MS Access:</strong></p>
					<pre>DROP INDEX index_name ON table_name</pre>
					<p><strong>DROP INDEX Syntax for MS SQL Server:</strong><p>
					<pre>DROP INDEX table_name.index_name</pre>
					<p><strong>DROP INDEX Syntax for DB2/Oracle:</strong></p>
					<pre>DROP INDEX index_name</pre>
					<p><strong>DROP INDEX Syntax for MySQL:</strong></p>
					<pre>ALTER TABLE table_name DROP INDEX index_name</pre>
				</li>
				<li>
					<h3><strong>The DROP TABLE Statement</strong></h3>
					<p>The DROP TABLE statement is used to delete a table.</p>
					<pre>DROP TABLE table_name</pre>
				</li>
				<li>
					<h3><strong>The DROP DATABASE Statement</strong></h3>
					<p>The DROP DATABASE statement is used to delete a database.</p>
					<pre>DROP DATABASE database_name</pre>
				</li>
			</ul>
		</li>
	</ul>
